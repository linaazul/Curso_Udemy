#Ao passar mais de uma informacao no print, ele da um pequeno espaço
print(12, 34)
#O print também pula linha automaticamente
print(56, 78)
#A função sep serve para definir algum separador entre as informacoes em um print
print(12, 34, sep=",")
#A função end serve para especificar como o seu print deve terminar
print(12, 34, end='##\n')