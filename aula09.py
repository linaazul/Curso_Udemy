#Introdução aos operadores aritimeticos
adicao = 10 + 10
print("Adição:", adicao)

subtracao = 10 - 5
print("Subtração:", subtracao)

multiplicacao = 10 * 10
print("Multiplicação:", multiplicacao)

divisao = 10 / 2  #sempre reorna float
print("Divisão:", divisao)

divisao_inteira = 10 // 2  #Ele arredonda o número caso necessario
print("Divisão inteira:", divisao_inteira)

exponenciacao = 10 ** 2
print("Exponenciação, número elevado a outro:", exponenciacao)

modulo = 25 % 5  # resto da divisao
print("Módulo:", modulo)