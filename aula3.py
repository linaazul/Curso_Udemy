# Nessa aula ele explica sobre strings, strings sao textos.
# Para fazer strings em python pode utilizar aspas simples ou duplas
print("STRING com aspas duplas")
print('string com aspas simples')

# Caracter de escape serve pra o seu interpretador nao interprete, apenas mostre
print("Teste com \"escape")

# Se por algum motivo eu quero que o caracter de escape seja exibido eu posso usar o r antes da aspas do print
print(r"Teste com \"escape\"")


